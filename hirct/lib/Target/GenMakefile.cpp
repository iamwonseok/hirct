#include "hirct/Target/GenMakefile.h"

#include <cerrno>
#include <cstring>
#include <fstream>
#include <iostream>
#include <sys/stat.h>

namespace hirct {

GenMakefile::GenMakefile(circt::hw::HWModuleOp hw_module,
                         const std::string &rtl_src,
                         const std::vector<std::string> &lib_dirs,
                         bool has_func_model)
    : hw_module_(hw_module), rtl_src_(rtl_src), lib_dirs_(lib_dirs),
      has_func_model_(has_func_model) {}

bool GenMakefile::emit(const std::string &output_dir) {
  std::string cmodel_dir = output_dir + "/cmodel";
  if (mkdir(cmodel_dir.c_str(), 0755) != 0 && errno != EEXIST) {
    std::cerr << "GenMakefile: cannot create directory: " << cmodel_dir << ": "
              << strerror(errno) << "\n";
    return false;
  }
  std::string verify_dir = output_dir + "/verify";
  if (mkdir(verify_dir.c_str(), 0755) != 0 && errno != EEXIST) {
    std::cerr << "GenMakefile: cannot create directory: " << verify_dir << ": "
              << strerror(errno) << "\n";
    return false;
  }

  std::string name = hw_module_.getSymName().str();
  std::string path = output_dir + "/Makefile";

  std::ofstream ofs(path);
  if (!ofs) {
    std::cerr << "GenMakefile: cannot open " << path << "\n";
    return false;
  }

  ofs << "# AUTO-GENERATED by hirct-gen \xe2\x80\x94 DO NOT EDIT\n";
  ofs << "# Makefile for C++ model: " << name << "\n\n";
  ofs << "CXX ?= g++\n";
  ofs << "CXXFLAGS ?= -std=c++17 -Wall -Wextra -O2\n";
  ofs << "VERILATOR ?= verilator\n\n";
  ofs << "MODULE := " << name << "\n\n";
  ofs << "SEED ?= 1\n";
  ofs << "CYCLES ?= 100\n";
  ofs << "RTL_SRC ?= " << rtl_src_ << "\n";
  ofs << "VERI_YDIRS ?=\n\n";
  ofs << "CMODEL_SRC := cmodel/$(MODULE).cpp\n";
  ofs << "CMODEL_HDR := cmodel/$(MODULE).h\n";
  ofs << "VERIFY_SRC := verify/verify_$(MODULE).cpp\n";
  ofs << "VERIFY_BIN := verify_$(MODULE)\n";
  ofs << "OBJ_DIR := obj_dir\n";
  ofs << "VM_PREFIX := V$(MODULE)\n\n";
  if (has_func_model_) {
    ofs << ".PHONY: test-compile test-compile-funcmodel test-verify "
           "test-verify-rebuild test-artifacts test\n\n";
  } else {
    ofs << ".PHONY: test-compile test-verify test-verify-rebuild test-artifacts "
           "test\n\n";
  }
  ofs << "test-compile: $(CMODEL_SRC) $(CMODEL_HDR)";
  if (has_func_model_)
    ofs << " test-compile-funcmodel";
  ofs << "\n";
  ofs << "\t$(CXX) $(CXXFLAGS) -c $(CMODEL_SRC) -o cmodel/$(MODULE).o\n\n";
  if (has_func_model_) {
    ofs << "test-compile-funcmodel:\n";
    ofs << "\t$(CXX) $(CXXFLAGS) -c func_model/*.cpp -I func_model/\n\n";
  }
  ofs << "$(VERIFY_BIN): $(VERIFY_SRC) $(CMODEL_SRC) $(CMODEL_HDR) "
         "$(RTL_SRC)\n";
  ofs << "\t@if [ -z \"$(RTL_SRC)\" ]; then \\\n";
  ofs << "\t  echo \"ERROR: RTL_SRC is empty. Re-run hirct-gen with Verilog "
         "input.\"; \\\n";
  ofs << "\t  exit 1; \\\n";
  ofs << "\tfi\n";
  ofs << "\t@if [ ! -f \"$(RTL_SRC)\" ]; then \\\n";
  ofs << "\t  echo \"ERROR: RTL source not found: $(RTL_SRC)\"; \\\n";
  ofs << "\t  exit 1; \\\n";
  ofs << "\tfi\n";
  ofs << "\t$(VERILATOR) --cc --trace --exe --build --Mdir $(OBJ_DIR) "
         "--top-module $(MODULE) "
         "-CFLAGS \"-I$$(pwd)/cmodel\" "
         "-o ../$(VERIFY_BIN) ";
  for (const auto &dir : lib_dirs_) {
    ofs << "-y " << dir << " ";
  }
  ofs << "$(VERI_YDIRS) "
         "$(RTL_SRC) $(VERIFY_SRC) $(CMODEL_SRC)\n\n";
  ofs << "test-verify: $(VERIFY_BIN)\n";
  ofs << "\t./$(VERIFY_BIN) $(CYCLES) $(SEED)\n\n";
  ofs << "test-verify-rebuild:\n";
  ofs << "\trm -rf $(OBJ_DIR) $(VERIFY_BIN)\n";
  ofs << "\t$(MAKE) test-verify SEED=$(SEED) CYCLES=$(CYCLES)\n\n";
  ofs << "test-artifacts:\n";
  ofs << "\t@test -f $(CMODEL_HDR)\n";
  ofs << "\t@test -f $(CMODEL_SRC)\n";
  ofs << "\t@test -f $(VERIFY_SRC)\n";
  if (has_func_model_) {
    ofs << "\t@test -f func_model/$(MODULE).h\n";
    ofs << "\t@test -f func_model/$(MODULE).cpp\n";
  }
  ofs << "\t@echo \"[test-artifacts] files exist for " << name << "\"\n\n";
  ofs << "test: test-compile test-verify test-artifacts\n";
  ofs << "\t@echo \"[test] all tests passed for " << name << "\"\n";

  return true;
}

} // namespace hirct
